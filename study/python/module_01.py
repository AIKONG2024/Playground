#관련 있는 변수, 함수, 클래스의 모음.: 모듈

#module: 함수 변수 클래스 등 파이썬 구성 요소 등을 모아놓은 파일

def add(x,y):
    return x+y

def subtrack(x,y):
    return x - y
def multiply(x, y):
    return x * y

def divide(x, y):
    return x/y

def power(x, y):
    return x ** y


print('-' * 15)
print('called! inner!')
print(add(5,5))
print(subtrack(15,5))
print(multiply(5,5))
print(divide(10,2))
print(power(5,3))
print('-' * 15)

